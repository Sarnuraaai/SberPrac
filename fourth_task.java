
public class Main
{
	public static void main(String[] args) {
	    for (int line = 1; line <= 3; ++line){
	        for (int column = 1; column <= 5; ++column){
	            if (column != 5){
	                System.out.print("* ");
	            }
	            else{
	                System.out.print("*");
	            }
	        }
	        System.out.print("\n");
	    }
	}
}
